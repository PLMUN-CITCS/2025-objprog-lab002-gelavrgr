// This program demonstrates variable declarations and case sensitivity in Java.
public class VariablesAndCaseSensitivity {
    public static void main(String[] args) {
        // Variable declarations
        int studentAge = 15;
        double itemPrice = 29.99;
        String studentName = "Alice";
        int counter = 10;
        int Counter = 20; // Different case

        // Output the values
        System.out.println("Student Age: " + studentAge);
        System.out.println("Item Price: $" + itemPrice);
        System.out.println("Student Name: " + studentName);
        System.out.println("Counter: " + counter);
        System.out.println("Counter (Different Case): " + Counter);
    } // Close the main method
} // Close the class}
